package com.mps.think.orderFunctionality.model;

public class DistributorModel {
	private int customerId; 
	private String company;       
	private String distributorReportPrefix;  
	private int parentDistributorId; 
	private String url;
	public int getCustomerId() {
		return customerId;
	}
	public void setCustomerId(int customerId) {
		this.customerId = customerId;
	}
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
	public String getDistributorReportPrefix() {
		return distributorReportPrefix;
	}
	public void setDistributorReportPrefix(String distributorReportPrefix) {
		this.distributorReportPrefix = distributorReportPrefix;
	}
	public int getParentDistributorId() {
		return parentDistributorId;
	}
	public void setParentDistributorId(int parentDistributorId) {
		this.parentDistributorId = parentDistributorId;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}

}
