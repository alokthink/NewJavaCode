package com.mps.think.setup.model;

public class PromotionCard {
	
	private int promotionCardId;
	private int ocId;
	private String promotionCard;
	private String description;
	
	public int getPromotionCardId() {
		return promotionCardId;
	}
	public void setPromotionCardId(int promotionCardId) {
		this.promotionCardId = promotionCardId;
	}
	public int getOcId() {
		return ocId;
	}
	public void setOcId(int ocId) {
		this.ocId = ocId;
	}
	public String getPromotionCard() {
		return promotionCard;
	}
	public void setPromotionCard(String promotionCard) {
		this.promotionCard = promotionCard;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	@Override
	public String toString() {
		return "PromotionCard [promotionCardId=" + promotionCardId + ", ocId=" + ocId + ", promotionCard="
				+ promotionCard + ", description=" + description + "]";
	}
	
}
