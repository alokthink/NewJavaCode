package com.mps.think.model;


public class BackIssuesModel {

	private String status;
	private String issue_date;
	private String qty_sent;
	private String qty_ordered;
	private String reason;
	private String issue_id;
	private String subscrip_id;
	private String rollback_job_id;
	private String issue_type;
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getIssue_date() {
		return issue_date;
	}
	public void setIssue_date(String issue_date) {
		this.issue_date = issue_date;
	}
	public String getQty_sent() {
		return qty_sent;
	}
	public void setQty_sent(String qty_sent) {
		this.qty_sent = qty_sent;
	}
	public String getQty_ordered() {
		return qty_ordered;
	}
	public void setQty_ordered(String qty_ordered) {
		this.qty_ordered = qty_ordered;
	}
	public String getReason() {
		return reason;
	}
	public void setReason(String reason) {
		this.reason = reason;
	}
	public String getIssue_id() {
		return issue_id;
	}
	public void setIssue_id(String issue_id) {
		this.issue_id = issue_id;
	}
	public String getSubscrip_id() {
		return subscrip_id;
	}
	public void setSubscrip_id(String subscrip_id) {
		this.subscrip_id = subscrip_id;
	}
	public String getRollback_job_id() {
		return rollback_job_id;
	}
	public void setRollback_job_id(String rollback_job_id) {
		this.rollback_job_id = rollback_job_id;
	}
	public String getIssue_type() {
		return issue_type;
	}
	public void setIssue_type(String issue_type) {
		this.issue_type = issue_type;
	}
	@Override
	public String toString() {
		return "BackIssuesModel [status=" + status + ", issue_date=" + issue_date + ", qty_sent=" + qty_sent
				+ ", qty_ordered=" + qty_ordered + ", reason=" + reason + ", issue_id=" + issue_id + ", subscrip_id="
				+ subscrip_id + ", rollback_job_id=" + rollback_job_id + ", issue_type=" + issue_type + "]";
	}
	
}
