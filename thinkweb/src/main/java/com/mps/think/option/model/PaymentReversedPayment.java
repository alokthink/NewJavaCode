package com.mps.think.option.model;

public class PaymentReversedPayment {
	 
	private Integer original_customer_id;
	private Integer original_payment_seq;
	private Integer reversed_customer_id;
	private Integer reversed_payment_seq;
	public Integer getOriginal_customer_id() {
		return original_customer_id;
	}
	public void setOriginal_customer_id(Integer original_customer_id) {
		this.original_customer_id = original_customer_id;
	}
	public Integer getOriginal_payment_seq() {
		return original_payment_seq;
	}
	public void setOriginal_payment_seq(Integer original_payment_seq) {
		this.original_payment_seq = original_payment_seq;
	}
	public Integer getReversed_customer_id() {
		return reversed_customer_id;
	}
	public void setReversed_customer_id(Integer reversed_customer_id) {
		this.reversed_customer_id = reversed_customer_id;
	}
	public Integer getReversed_payment_seq() {
		return reversed_payment_seq;
	}
	public void setReversed_payment_seq(Integer reversed_payment_seq) {
		this.reversed_payment_seq = reversed_payment_seq;
	}
	@Override
	public String toString() {
		return "PaymentReversedPayment [original_customer_id=" + original_customer_id + ", original_payment_seq="
				+ original_payment_seq + ", reversed_customer_id=" + reversed_customer_id + ", reversed_payment_seq="
				+ reversed_payment_seq + "]";
	}
	
	
}
