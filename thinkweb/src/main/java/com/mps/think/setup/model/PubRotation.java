package com.mps.think.setup.model;

public class PubRotation {

	private Integer pubRotationId;
	private Integer ocId;
	private String description;
	private String onString;
	private String onCalendarUnit;
	private Short onDay;
	
	public Integer getPubRotationId() {
		return pubRotationId;
	}
	public void setPubRotationId(Integer pubRotationId) {
		this.pubRotationId = pubRotationId;
	}
	public Integer getOcId() {
		return ocId;
	}
	public void setOcId(Integer ocId) {
		this.ocId = ocId;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getOnString() {
		return onString;
	}
	public void setOnString(String onString) {
		this.onString = onString;
	}
	public String getOnCalendarUnit() {
		return onCalendarUnit;
	}
	public void setOnCalendarUnit(String onCalendarUnit) {
		this.onCalendarUnit = onCalendarUnit;
	}
	public Short getOnDay() {
		return onDay;
	}
	public void setOnDay(Short onDay) {
		this.onDay = onDay;
	}
}
