package com.mps.think.orderFunctionality.model;

import java.util.List;

import com.fasterxml.jackson.annotation.JsonInclude;

@JsonInclude(JsonInclude.Include.NON_NULL)
public class DemographicChildModel {

    private String rootname;
	private String Date;
	private String descQDefault;
	
	private int demQuestionId;

	private List<DemographicChildModel> nodes;
	private DemoModel data;
    
	public String getRootname() {
		return rootname;
	}

	public void setRootname(String rootname) {
		this.rootname = rootname;
	}

	public String getDescQDefault() {
		return descQDefault;
	}

	public void setDescQDefault(String descQDefault) {
		this.descQDefault = descQDefault;
	}

	public String getDate() {
		return Date;
	}

	public void setDate(String date) {
		Date = date;
	}

	public int getDemQuestionId() {
		return demQuestionId;
	}

	public void setDemQuestionId(int demQuestionId) {
		this.demQuestionId = demQuestionId;
	}

	public DemoModel getData() {
		return data;
	}

	public void setData(DemoModel data) {
		this.data = data;
	}

	public List<DemographicChildModel> getNodes() {
		return nodes;
	}

	public void setNodes(List<DemographicChildModel> nodes) {
		this.nodes = nodes;
	}


}
