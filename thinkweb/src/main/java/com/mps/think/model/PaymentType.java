package com.mps.think.model;

import java.util.List;


public class PaymentType {
	
	String paymentType;
	List<String> paymentTypeList;

	public List<String> getPaymentTypeList() {
		return paymentTypeList;
	}

	public void setPaymentTypeList(List<String> paymentTypeList) {
		this.paymentTypeList = paymentTypeList;
	}

	public String getPaymentType() {
		return paymentType;
	}

	public void setPaymentType(String paymentType) {
		this.paymentType = paymentType;
	}

	@Override
	public String toString() {
		return "PaymentType [paymentType=" + paymentType + ", paymentTypeList="
				+ paymentTypeList + "]";
	}

}
